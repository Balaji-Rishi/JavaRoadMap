package demo.java.Arrays;

public class TwoDArray {
    public static void main(String[] args) {
        int[][] matrix = {
            {1, 2, 3},
            {4, 5, 6},
            {7, 8, 9}
        };

        System.out.println("Element at row 1, column 2: " + matrix[1][2]); // Output: 6
    }
}

