package demo.java.String.StringMethods;

public class IndexExample {
    public static void main(String[] args) {
        String str = "Programming in Java";

        System.out.println(str.indexOf('a')); // Output: 4
        System.out.println(str.lastIndexOf('a')); // Output: 18
        System.out.println(str.indexOf("Java")); // Output: 15
    }
}

