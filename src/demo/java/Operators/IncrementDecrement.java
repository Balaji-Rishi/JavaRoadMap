package demo.java.Operators;

public class IncrementDecrement {
	
	  public static void main(String[] args) {
	        int num = 5;
	        System.out.println("Pre-Increment: " + (++num)); // 6
	        System.out.println("Post-Increment: " + (num++)); // 6, then num becomes 7
	        System.out.println("Current Value: " + num); // 7
	    }

}
