package demo.java.Operators;

public class RelationalOperators {
	
	  public static void main(String[] args) {
	        int x = 10, y = 5;
	        System.out.println(x == y); // false
	        System.out.println(x != y); // true
	        System.out.println(x > y);  // true
	        System.out.println(x < y);  // false
	        System.out.println(x >= y); // true
	        System.out.println(x <= y); // false
	    }

}
