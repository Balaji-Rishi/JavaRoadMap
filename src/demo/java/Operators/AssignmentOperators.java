package demo.java.Operators;

public class AssignmentOperators {
	
	  public static void main(String[] args) {
	        int x = 10;
	        x += 5; // x = x + 5
	        System.out.println("x += 5: " + x);
	        x -= 3; // x = x - 3
	        System.out.println("x -= 3: " + x);
	        x *= 2; // x = x * 2
	        System.out.println("x *= 2: " + x);
	    }

}
